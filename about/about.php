    <p><i class="fa fa-quote-left fa-lg"></i> Through the mission and vision of the organization, Circle K International is dedicated to the realization of humankind’s potential. The potential of Circle K International lies in its ability to positively influence members of society who are facing ultimate personal decisions and those who will one day create the vision of mankind for generations to come. Leadership opportunities afford Circle K members the resources and tools needed to become active citizens. Circle K’ers can assume leadership responsibilities at all levels of the organization and through various experiential training conferences while actively contributing to the running of their respective clubs, divisions, and communities. The unique experiences offered by Circle K truly develop and bring out the leader within each individual. <i class="fa fa-quote-right fa-lg"></i></p>
