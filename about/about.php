    <h2>Service</h2>
    <p><i class="fa fa-quote-left fa-lg"></i> As one of the three tenets, service is the fundamental element of Circle K International. Collectively, Circle K members perform more than one million hours of service on their campuses and in their communities annually. Without service, Circle K International would be just another campus activity. Through service, college students are making the world a better place one service project at a time. Together, Circle K'ers truly make a difference in the community. <i class="fa fa-quote-right fa-lg"></i></p>
