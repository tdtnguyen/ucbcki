    <p><i class="fa fa-quote-left fa-lg"></i> If there is one thing that Circle K’ers know best, it is fellowship. Whether they are planning a car wash, visiting shut-ins, reading to three-year-olds or conducting business, Circle K members across the globe take time to meet and welcome new people. With each element and aspect of Circle K International, members experience genuine fellowship and develop life-long relationships with fellow collegians, advisors, Kiwanians and citizens in their communities. Whether a Circle K’er is mentoring a child, networking with a businessman, or bowling with members, that Circle K’er is developing social skills, meeting new people, creating meaningful friendships, and strengthening relationships. <i class="fa fa-quote-right fa-lg"></i></p>
