        <h2>Start with a Story</h2>
        <p>On every 2nd and 4th Sunday of the month, our club goes out to the Glenn Dyer detention facility in Oakland for Start with a Story in order to encourage literacy in young children and teens. As they wait in the waiting area, they have the opportunity to walk over to our book rack and check out any books we have; we have all sorts of books for a wide range of ages and interests, including picture books, books on sports, books on animals, chapter books, and more! Every child or teen who visits us when we are volunteering are allowed to take one book home for free, or they can ask one of us to read a book out loud to them while they are waiting.
Every time we go, children are always excited to find us pulling the book rack from the storage area to the waiting area! Parents also show their gratitude to us and appreciate the opportunity to get free books for their children to read at home. So, if you love books, like working with children, and want to promote child literacy, you should sign up for Start with a Story on the website calendar!</p>
